export interface Influencer {
  id: string;
  name: string;
  username: string;
  category: string;
  categories?: string[]; // Multiple categories support
  location: string;
  bio: string;
  profileImage: string;
  coverImage: string;
  verified: boolean;
  featured: boolean;
  platforms: {
    instagram?: number;
    tiktok?: number;
    youtube?: number;
  };
  startingPrice: number;
  packages: Package[];
  portfolio: PortfolioItem[];
  rating: number;
  totalOrders: number;
}

export interface Package {
  id: string;
  type: string;
  description: string;
  deliverables: string[];
  price: number;
  deliveryTime: string;
}

export interface PortfolioItem {
  id: string;
  type: 'image' | 'video';
  url: string;
  platform: string;
}

export interface Category {
  id: string;
  name: string;
  icon: string;
  count: number;
}

export interface Campaign {
  id: string;
  name: string;
  influencer: string;
  status: 'active' | 'pending' | 'completed' | 'cancelled';
  budget: number;
  startDate: string;
  endDate: string;
}

